/*
    In C#, "Console.WriteLine()" is a method used to display text on the console window. It takes one or more arguments, which can be strings, variables, or expressions, and prints them to the console followed by a line break.
*/

// Here's an example of using "Console.WriteLine()" to display a message on the console:
Console.WriteLine("Hello, world!");

// In this example, the string "Hello, world!" is passed as an argument to the "Console.WriteLine()" method. When the program runs, this string is printed to the console followed by a line break.


// You can also include variables or expressions in the arguments of "Console.WriteLine()" to display their values on the console. For example:
int x = 10;
int y = 20;
int sum = x + y;

Console.WriteLine("The sum of " + x + " and " + y + " is " + sum);

// In this example, the values of the variables "x", "y", and "sum" are included in the argument of "Console.WriteLine()" using string concatenation. When the program runs, the message "The sum of 10 and 20 is 30" is printed to the console.