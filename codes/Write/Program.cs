/*
    In C#, the "Write" method is used to output data to the console window. It belongs to the "Console" class, which is part of the "System" namespace. Here is an example of how you can use the "Write" method to output the string "Hello, world!" to the console:
*/

Console.Write("Hello, world!");

// In this example, we first include the "System" namespace with the "using" directive. Then we define a class called "Program" with a "Main" method. Inside the "Main" method, we call the "Write" method of the "Console" class and pass it the string "Hello, world!" as an argument. This causes the string to be displayed in the console window.


